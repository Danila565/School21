char	*ft_strcpy(char *dest, char *src)
{
	int	sz_s;

	sz_s = 0;
	while (src[sz_s] != '\0')
	{
		dest[sz_s] = src[sz_s];
		++sz_s;
	}
	dest[sz_s] = src[sz_s];
	return (dest);
}
