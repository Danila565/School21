long long	plus(long long a, long long b)
{
	return (a + b);
}

long long	minus(long long a, long long b)
{
	return (a - b);
}

long long	multi(long long a, long long b)
{
	return (a * b);
}

long long	div(long long a, long long b)
{
	return (a / b);
}

long long	mod(long long a, long long b)
{
	return (a % b);
}
