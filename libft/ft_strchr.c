char	*ft_strchr(const char *s, int a)
{
	char	c;
	char	*p;

	p = (char *) s;
	c = (char) a;
	while (*p != '\0')
	{
		if (c == *p)
			return (p);
		++p;
	}
	if (c == *p)
		return (p);
	return ((void *)(0));
}
